                    <h2 style="text-align :center; font-weight: 500;">ABOUT DEPARTMENT</h2>
                    <h2>About Mechanical Engineering</h2>
                    <p>Mechanical engineering is a discipline of engineering that applies the principles of physics and materials science for analysis, design, manufacturing, and maintenance of mechanical systems. It is the branch of engineering that involves the production and usage of heat and mechanical power for the design, production, and operation of machines and tools. It is one of the oldest and broadest engineering disciplines. The engineering field requires an understanding of core concepts including mechanics, kinematics, thermodynamics, materials science, and structural analysis. Mechanical engineers use these core principles along with tools like computer-aided engineering and product lifecycle management to design and analyze manufacturing plants, industrial equipment and machinery, heating and cooling systems, transport systems, aircraft, watercraft, robotics, medical devices and more.</p>
                    
                    <p>Mechanical engineers today are pursuing developments in fields such as composites, mechatronics, and nanotechnology. Mechanical engineering overlaps with aerospace engineering, civil engineering, electrical engineering, petroleum engineering, and chemical engineering to varying amounts.</p>
                    
                    <p>Like other career paths, entry into this particular branch of engineering also requires certain basic traits. First and foremost, you must have a penchant for mechanical components and machinery and how these machines work. You must be good at physics, chemistry, and mathematics. You must also have skills such as an analytical bent of mind, logical reasoning, and problem solving.</p>

                    <h2>Scope</h2>
                    <p>Mechanical Engineers are required in all manufacturing facilities. The working criteria of a mechanical engineer changes according to the type and domain of the company they are working with and the area of specialization. In a broader sense, it can be said that a mechanical engineer works on the design and control of a system that goes into the process of manufacturing the machinery and product. He tests new systems for feasibility and efficiency and carries out quality management and improvement processes.</p>

                    <p>There is tremendous scope for mechanical engineers in automobile engineering, cement industry, steel, power sector, hydraulics, manufacturing plants, drilling and mining industry, petroleum, aeronautical, biotechnology, and many more. Nowadays they are also increasingly needed in the environmental and bio-medical fields.</p>
                    
                    <p>A beginner in Mechanical Engineering can opt for various job openings such as: Maintenance Engineer, Safety Engineer, Quality Assurance, CNC Programmer, Jr. Engineer, Design Engineer, CAD/CAM Trainer, Production Supervisor/Engineer, R&D Trainee, etc.</p>
